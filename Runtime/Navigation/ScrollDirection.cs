namespace Zigurous.UI
{
    /// <summary>
    /// A scroll direction.
    /// </summary>
    public enum ScrollDirection
    {
        /// <summary>
        /// Scrolls in the y-axis.
        /// </summary>
        Vertical,

        /// <summary>
        /// Scrolls in the x-axis.
        /// </summary>
        Horizontal,

        /// <summary>
        /// Scrolls in both the x-axis and the y-axis.
        /// </summary>
        Both,
    }

}
